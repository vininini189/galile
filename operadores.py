#operação de soma em python
num1 =int(input("digite o numero1 desejado "))#tranformei uma str(texto) em int(numero)
num2 =int(input("digite o numero2 desejado "))
soma = num1 + num2 

print(f'o resultado da soma de num1 com num2 é: {soma}')

#operação de subtração
subtração = num1 - num2
print(f'o resultado é {subtração}')

#operação de multiplicação
mult = num1 * num2
print(f'o resultado da mult de num1 com num2 é: {mult} ')

#operação de divisão     /
div = num1 / num2
print(f'o resultado da div de num1 com num2 é: {div:.2f} ')

#operação de resto%
rest = num1 % num2
print(f'o resultado do resto de num1 com num2 é: {rest} ')